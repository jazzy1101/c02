int	ft_str_is_lowercase(char *str)
{
	int	i;
	
	i = 0;
	while(*str)
	{
		if (!str[i])
			return (1);
		if (str[i] >= 'a' && str[i] <= 'z')
			i++;
		else
			return (0);
	}
	return (1);
}
