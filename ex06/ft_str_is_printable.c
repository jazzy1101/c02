int	ft_str_is_printable(char *str)
{
	int	i;

	i = 0;
	while(*str)
	{
		if(!str[i])
			return (1);
		if(str[i] >= ' ' && str[i] <= '~')
			i++;
		else
			return (0);
	}
	return (1);
}
